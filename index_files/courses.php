      <h3 class="bg-dark text-white pb-2 pt-1 pl-3" id="gatecs">About GATE (Computer Science)</h3>
      <p class="lead">
        Graduate Aptitude Test in Engineering (GATE) is an examination conducted jointly by the Indian Institute of Science (IISc), Bangalore and the seven Indian Institutes of Technology (at Bombay, Delhi, Guwahati, Kanpur, Kharagpur, Madras and Roorkee) on behalf of the National Coordination Board (NCB)-GATE, Department of Higher Education, Ministry of Human Resource Development (MHRD), Government of India.
        Qualifying in GATE is a mandatory requirement for seeking admission and/or financial assistance to:
        <ul class="lead">
          <li>Master’s programs and direct Doctoral programs in Engineering/Technology/Architecture and </li>
          <li>Doctoral programs in relevant branches of Science, in the institutions supported by the MHRD and other Government agencies. Even in some colleges and institutions, which admit students without MHRD scholarship/assistantship, the GATE qualification is mandatory. Further, many Public Sector Undertakings (PSUs) have been using the GATE score in their recruitment process.</li>
        </ul>
      </p>
