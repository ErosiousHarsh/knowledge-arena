      <h3 class="bg-dark text-white pb-2 pt-1 pl-3">About NIMCET MCA</h3>
      <p class="lead">
        The NIMCET is a National Level Entrance Test conducted by NITs for admission to the MCA course offered by NITs. The admission to MCA programme in National Institute of Technologies (NITs), offered MCA Course, is based on the Rank obtained in NIMCET.
      </p>
      <span style="color:green;">Eligibility:</span>
      <p class="lead">
        Candidates, with 60% marks (6.5/10 CGPA) in Bachelor’s degree of Full Time 3 years duration from a recognized University with Mathematics / Statistics / Business Maths. as one of the subjects in any one semester/year, both in graduation and at 10+2 level are eligible. For SC/ST candidates the percentage of marks required is 55%(6.0/10 CGPA). Those candidates who are appearing for the qualifying examinations are also eligible to appear for the test. Indian Nationals only are eligible to appear for the NIMCET.
      </p>
