					<h3 class="text-heading title_color">NIMCET MCA Exam Syllabus</h3>
                        <b>Mathematics</b>
                        <ul>
                            <li><u>Algebra:</u> Fundamental operations in algebra, expansions, factorization, simultaneous
linear / quadratic equations, indices, logarithms, arithmetic, geometric and harmonic
progressions, determinants and matrices</li>
                            <li><u>Coordinate Geometry:</u> Rectangular Cartesian coordinates, distance formulae, equation
of a line, and intersection of lines, pair of straight lines, equations of a circle,
parabola, ellipse, and hyperbola.</li>
                            <li><u>Set Theory:</u> Concept of sets – Union, Intersection, Cardinality, Elementary counting;
permutations and combinations.</li>
                            <li><u>Probability and Statistics:</u> Basic concepts of probability theory, Averages, Dependent
and independent events, frequency distributions, measures of central tendencies and
dispersions.</li>
                            <li><u>Calculus:</u> Limit of functions, continuous function, differentiation of function, tangents
and normals, simple examples of maxima and minima. Integration of functions by
parts, by substitution and by partial fraction, definite integrals, applications of definite
integrals to areas.</li>
                            <li><u>Trigonometry:</u> Simple identities, trigonometric equations properties of triangles,
solution of triangles, heights and distances, general solutions of trigonometric
equations.</li>
                            <li><u>Vectors:</u> Position vector, addition and subtraction of vectors, scalar and vector
products and their applications to simple geometrical problems and mechanics.</li>
                        </ul>

                    <b>Analytical Ability And Logical Reasoning</b>
                    <ul>
                        <li>Puzzles and Verbal Reasoning</li>
                        <li>Logical Reasoning</li>
                        <li>Data Sufficiency</li>
                        <li>Non-Verbal Reasoning</li>
                        <li>Data Interpretation</li>
                        <li>Analytical Reasoning</li>
                    </ul>

                    <b>Computer Awareness</b>
                    <ul>
                        <li><u>Data Representation:</u> Representation of characters, integers and fractions, binary and
hexadecimal representations, binary arithmetic: addition, subtraction, multiplication,
division, simple arithmetic and two’s complement arithmetic, floating point
representation of numbers, Boolean algebra, truth tables, Venn diagrams.</li>
                        <li><u>Computer Basics:</u> Organization of a computer, Central Processing Unit (CPU),
structure of instructions in CPU, input/output devices, computer memory, and backup devices</li>
                    </ul>

                    <b>General English</b>
                    <ul>
                        <li>Synonyms and Antonyms</li>
                        <li>Meaning of words and phrases</li>
                        <li>Comprehension</li>
                        <li>Vocabulary</li>
                        <li>Basic English Grammar (like usage of correct forms of verbs, prepositions and
articles)</li>
                        <li>Word power</li>
                        <li>Technical writing</li>
