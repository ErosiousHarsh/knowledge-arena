			<section class="sample-text-area2">
				<div class="container">
					<h3 class="text-heading title_color">GATE Computer Science Exam Syllabus</h3>
                        <b>Engineering Mathematics</b>
                        <ul>
                            <li><u>Discrete Mathematics:</u> Propositional and first order logic. Sets, relations, functions, partial orders and
lattices. Groups. Graphs: connectivity, matching, coloring. Combinatorics: counting, recurrence
relations, generating functions.</li>
                            <li><u>Linear Algebra:</u> Matrices, determinants, system of linear equations, eigenvalues and eigenvectors, LU
decomposition</li>
                            <li><u>Calculus:</u> Limits, continuity and differentiability. Maxima and minima. Mean value theorem. Integration.</li>
                            <li><u>Probability:</u>  Random variables. Uniform, normal, exponential, poisson and binomial distributions. Mean,
median, mode and standard deviation. Conditional probability and Bayes theorem.</li>
                        </ul>

                    <b>Computer Science and Information Technology</b>
                    <ul>
                        <li><u>Digital Logic:</u> Boolean algebra. Combinational and sequential circuits. Minimization. Number representations and
computer arithmetic (fixed and floating point).</li>
                        <li><u>Computer Organization and Architecture:</u> Machine instructions and addressing modes. ALU, data‐path and control unit. Instruction pipelining.
Memory hierarchy: cache, main memory and secondary storage; I/O interface (interrupt and DMA
mode).</li>
                        <li><u>Programming and Data Structures:</u> Programming in C. Recursion. Arrays, stacks, queues, linked lists, trees, binary search trees, binary
heaps, graphs.</li>
                        <li><u>Algorithms:</u> Searching, sorting, hashing. Asymptotic worst case time and space complexity. Algorithm design
techniques: greedy, dynamic programming and divide‐and‐conquer. Graph search, minimum spanning trees, shortest paths.</li>
                        <li><u>Theory of Computation:</u> Regular expressions and finite automata. Context-free grammars and push-down automata. Regular and contex-free languages, pumping lemma. Turing machines and undecidability.
</li>
                        <li><u>Compiler Design:</u> Lexical analysis, parsing, syntax-directed translation. Runtime environments. Intermediate code
generation.</li>
                        <li><u>Operating System:</u> Processes, threads, inter‐process communication, concurrency and synchronization. Deadlock. CPU
scheduling. Memory management and virtual memory. File systems.</li>
                        <li><u>Databases:</u> ER‐model. Relational model: relational algebra, tuple calculus, SQL. Integrity constraints, normal forms.
File organization, indexing (e.g., B and B+ trees). Transactions and concurrency control.
</li>
                        <li><u>Computer Networks:</u> Concept of layering. LAN technologies (Ethernet). Flow and error control techniques, switching.
IPv4/IPv6, routers and routing algorithms (distance vector, link state). TCP/UDP and sockets, congestion
control. Application layer protocols (DNS, SMTP, POP, FTP, HTTP). Basics of Wi-Fi. Network
security: authentication, basics of public key and private key cryptography, digital signatures and
certificates, firewalls.</li>
                    </ul>
                    
